-- Diagnostic nav (new API): jumpgnostics and filtered versions (errors, warnings)
local function diagnostic_jump(delta, severity)
  return function()
    vim.diagnostic.jump({
      count = delta, -- +1 forward, -1 back
      severity = severity and vim.diagnostic.severity[severity] or nil,
      wrap = true, -- loop around file (default)
    })
  end
end
map("n", "<leader>cd", vim.diagnostic.open_float, { desc = "Line Diagnostics" })
map("n", "]d", diagnostic_jump(1), { desc = "Next Diagnostic" })
map("n", "[d", diagnostic_jump(-1), { desc = "Prev Diagnostic" })
map("n", "]e", diagnostic_jump(1, "ERROR"), { desc = "Next Error" })
map("n", "[e", diagnostic_jump(-1, "ERROR"), { desc = "Prev Error" })
map("n", "]w", diagnostic_jump(1, "WARN"), { desc = "Next Warning" })
map("n", "[w", diagnostic_jump(-1, "WARN"), { desc = "Prev Warning" })

-- Auto-format toggles that ... conform :D with conform.nvim (taken from recipes)
-- Helper: build a Snacks toggle object
local function autoformat_toggle(buf)
  return Snacks.toggle({
    name = "Auto Format (" .. (buf and "Buffer" or "Global") .. ")",
    get = function()
      if buf then
        -- can be nil if not set, better catch that
        return vim.b.disable_autoformat ~= true
      end
      return vim.g.disable_autoformat ~= true
    end,
    set = function(state)
      if buf then
        vim.b.disable_autoformat = not state
      else
        vim.g.disable_autoformat = not state
      end
    end,
  })
end
autoformat_toggle(false):map("<leader>uf") -- global toggle
autoformat_toggle(true):map("<leader>uF") -- buffer-local toggle

