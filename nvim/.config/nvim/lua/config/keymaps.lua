-- Magic-Escape: clear hlsearch ✚ stop snippets (LuaSnip or vim.snippet)
map({ "i", "n", "s" }, "<Esc>", function()
  vim.cmd("nohlsearch") -- turn off search highlight
  -- try the built-in snippet API first
  -- (dont't know what blink.cmp uses yet)
  if vim.snippet and vim.snippet.active() then
    vim.snippet.stop() -- exits any vim.snippet jump
  else
    -- fall back to LuaSnip if that’s the engine in use
    local ok, ls = pcall(require, "luasnip")
    if ok and ls.in_snippet() then
      ls.unlink_current() -- abort current LuaSnip node
    end
  end
  return "<Esc>" -- still leave the current mode
end, { expr = true, desc = "Esc: clear search & stop snippet" })

